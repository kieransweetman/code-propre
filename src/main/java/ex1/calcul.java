package ex1;

/**
 * Calculator class
 * 
 * @author kieran sweetman
 */
public class calcul {

	/**
	 * Returns the sum of arguments
	 * 
	 * @param a first int
	 * @param b second int
	 * @return sum of a and b
	 */
	public int get1(int a, int b) {
		return a + b;
	}

	/**
	 * Returns the different of arguments
	 * 
	 * @param a first int
	 * @param b second int
	 * @return difference of a and b
	 */
	public int get2(int a, int b) {
		return a - b;
	}
}
